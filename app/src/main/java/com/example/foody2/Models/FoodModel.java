package com.example.foody2.Models;

public class FoodModel {
    int pic2;
    String text3;
    String text4;
    String text5;

    public FoodModel(int pic2, String text3, String text4, String text5) {
        this.pic2 = pic2;
        this.text3 = text3;
        this.text4 = text4;
        this.text5 = text5;
    }

    public int getPic2() {
        return pic2;
    }

    public void setPic2(int pic2) {
        this.pic2 = pic2;
    }

    public String getText3() {
        return text3;
    }

    public void setText3(String text3) {
        this.text3 = text3;
    }

    public String getText4() {
        return text4;
    }

    public void setText4(String text4) {
        this.text4 = text4;
    }

    public String getText5() {
        return text5;
    }

    public void setText5(String text5) {
        this.text5 = text5;
    }
}
